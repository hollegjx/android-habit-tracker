@Composable
fun EnhancedChatInputBar(
    inputText: String,
    onInputChange: (String) -> Unit,
    onSendMessage: () -> Unit,
    isLoading: Boolean = false,
    modifier: Modifier = Modifier
) {
    var isMenuExpanded by remember { mutableStateOf(false) }
    val keyboardController = LocalSoftwareKeyboardController.current
    val focusManager = LocalFocusManager.current
    val focusRequester = remember { FocusRequester() }
    
    // Â§ÑÁêÜËæìÂÖ•Ê°ÜÁÇπÂáªÔºöÈöêËóèËèúÂçïÔºåÊòæÁ§∫ÈîÆÁõò
    val handleInputFocus = {
        if (isMenuExpanded) {
            isMenuExpanded = false
        }
    }
    
    // Â§ÑÁêÜÂä†Âè∑ÊåâÈíÆÁÇπÂáªÔºöÈöêËóèÈîÆÁõòÔºåÊòæÁ§∫ËèúÂçï
    val handlePlusClick = {
        keyboardController?.hide()
        focusManager.clearFocus()
        isMenuExpanded = !isMenuExpanded
    }
    
    Column(
        modifier = modifier.fillMaxWidth()
    ) {
        // ÂºπÂá∫ËèúÂçï
        if (isMenuExpanded) {
            AttachmentMenu(
                onImageClick = { /* TODO: ‰∏ä‰º†ÂõæÂÉè */ },
                onFileClick = { /* TODO: ‰∏ä‰º†Êñá‰ª∂ */ },
                onRecordClick = { /* TODO: ‰∏ä‰º†ËÆ∞ÂΩï */ },
                onDismiss = { isMenuExpanded = false }
            )
        }
        
        // ËæìÂÖ•Ê†è
        Box(
            modifier = Modifier
                .fillMaxWidth()
                .background(
                    brush = Brush.verticalGradient(
                        colors = listOf(
                            Color.Transparent,
                            Color.Black.copy(alpha = 0.1f)
                        )
                    )
                )
                .padding(16.dp)
                // ÁßªÈô§imePadding()ÔºåÂõ†‰∏∫ScaffoldÁöÑbottomBarÂ∑≤ÁªèËá™Âä®Â§ÑÁêÜÈîÆÁõò‰ΩçÁΩÆ
        ) {
            Row(
                modifier = Modifier.fillMaxWidth(),
                verticalAlignment = Alignment.Bottom,
                horizontalArrangement = Arrangement.spacedBy(8.dp)
            ) {
                // ËæìÂÖ•Ê°Ü
                OutlinedTextField(
                    value = inputText,
                    onValueChange = { 
                        onInputChange(it)
                        handleInputFocus()
                    },
                    placeholder = { 
                        Text(
                            "ËæìÂÖ•Ê∂àÊÅØ...",
                            color = Color.White.copy(alpha = 0.6f)
                        )
                    },
                    modifier = Modifier
                        .weight(1f)
                        .focusRequester(focusRequester),
                    shape = RoundedCornerShape(24.dp),
                    colors = OutlinedTextFieldDefaults.colors(
                        focusedBorderColor = Color.White.copy(alpha = 0.5f),
                        unfocusedBorderColor = Color.White.copy(alpha = 0.3f),
                        focusedTextColor = Color.White,
                        unfocusedTextColor = Color.White,
                        cursorColor = Color.White
                    ),
                    maxLines = 4
                )
                
                // ÂèëÈÄÅÊåâÈíÆ
                Button(
                    onClick = onSendMessage,
                    enabled = inputText.isNotBlank() && !isLoading,
                    modifier = Modifier.size(48.dp),
                    shape = CircleShape,
                    colors = ButtonDefaults.buttonColors(
                        containerColor = if (inputText.isNotBlank() && !isLoading) GradientStart else Color.Gray,
                        contentColor = Color.White
                    ),
                    contentPadding = PaddingValues(0.dp)
                ) {
                    if (isLoading) {
                        CircularProgressIndicator(
                            modifier = Modifier.size(20.dp),
                            color = Color.White,
                            strokeWidth = 2.dp
                        )
                    } else {
                        Icon(
                            imageVector = Icons.Default.Send,
                            contentDescription = "ÂèëÈÄÅ",
                            modifier = Modifier.size(20.dp)
                        )
                    }
                }
                
                // Âä†Âè∑ÊåâÈíÆÔºàÈôÑ‰ª∂ÂäüËÉΩÔºâ
                Button(
                    onClick = handlePlusClick,
                    modifier = Modifier.size(48.dp),
                    shape = CircleShape,
                    colors = ButtonDefaults.buttonColors(
                        containerColor = if (isMenuExpanded) GradientStart else Color.White.copy(alpha = 0.3f),
                        contentColor = Color.White
                    ),
                    contentPadding = PaddingValues(0.dp)
                ) {
                    Icon(
                        imageVector = Icons.Default.Add,
                        contentDescription = "ÈôÑ‰ª∂",
                        modifier = Modifier.size(20.dp)
                    )
                }
            }
        }
    }
